<p><em>Mapping Movement in American History and Culture</em> is a publication of the Newberry Library’s Hermon Dunlap Smith Center for the History of Cartography. This online archive and companion to the study, interpretation, and educational use of American maps of movement features more than five hundred high-resolution images of maps drawn from the extensive collections of the Newberry Library dating from the sixteenth to the twenty-first century. The archive is fully searchable by a variety of topics, including mode and context of travel, map format and genre, authors and publishers, geographic area, subject, and date. Accompanying thematic essays and image captions by leading scholars in their fields offer a variety of perspectives on the history and technology of American travel, transportation, commerce, and communications.</p>
